# 4. Proposal persistence
# ===========================================================================

class TestProposalPersistence(unittest.TestCase):

    def test_save_and_get(self):
        ws = _workspace("persist")
        gen = CapabilityGenerator(_failing_llm(), _registry(), ws / "proposals")
        gen.generate_proposal({"capability_id": "my_cap", "domain": "web", "description": "x"})
        loaded = gen.get_proposal("my_cap")
        self.assertIsNotNone(loaded)
        self.assertEqual(loaded["id"], "my_cap")

    def test_get_nonexistent_returns_none(self):
        ws = _workspace("no_prop")
        gen = CapabilityGenerator(_failing_llm(), _registry(), ws / "proposals")
        self.assertIsNone(gen.get_proposal("ghost"))

    def test_list_proposals(self):
        ws = _workspace("list_props")
        gen = CapabilityGenerator(_failing_llm(), _registry(), ws / "proposals")
        gen.generate_proposal({"capability_id": "cap_alpha", "domain": "ejecucion", "description": "a"})
        gen.generate_proposal({"capability_id": "cap_beta", "domain": "ejecucion", "description": "b"})
        ids = gen.list_proposals()
        self.assertIn("cap_alpha", ids)
        self.assertIn("cap_beta", ids)

    def test_delete_proposal(self):
        ws = _workspace("delete_prop")
        gen = CapabilityGenerator(_failing_llm(), _registry(), ws / "proposals")
        gen.generate_proposal({"capability_id": "del_cap", "domain": "ejecucion", "description": "x"})
        self.assertTrue(gen.delete_proposal("del_cap"))
        self.assertIsNone(gen.get_proposal("del_cap"))
        self.assertFalse(gen.delete_proposal("del_cap"))


