class TestNoRegistryMutation(unittest.TestCase):

    def test_registry_unchanged_after_generation(self):
        ws = _workspace("no_mutate")
        reg = _registry()
        initial_ids = set(reg.ids())
        gen = CapabilityGenerator(_failing_llm(), reg, ws / "proposals")
        gen.generate_proposal({"capability_id": "new_cap", "domain": "ejecucion", "description": "x"})
        self.assertEqual(set(reg.ids()), initial_ids)


